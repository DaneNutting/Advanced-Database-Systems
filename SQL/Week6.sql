        FOREIGN KEY (T_ID) REFERENCES DW1_TIME(T_ID),
        FOREIGN KEY (P_ID) REFERENCES DW1_PRODUCT(P_ID),
        FOREIGN KEY (S_ID) REFERENCES DW1_SHOP(S_ID) 
   )
/


